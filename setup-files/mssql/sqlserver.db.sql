CREATE VIEW TOOL_PERMISSION AS 
SELECT PROJECT_ID, TOOL_ID,USER_ID, 
IIF(MAX(IIF(ACCESS_EXECUTE = 1,1,-1) * IIF(USER_ID IS NULL,1,10))>0,1,0) AS ACCESS_EXECUTE,
IIF(MAX(IIF(ACCESS_VIEW_HISTORY = 1,1,-1) * IIF(USER_ID IS NULL,1,10))>0,1,0) AS ACCESS_VIEW_HISTORY
FROM DBM_TOOL_PERMISSION 
GROUP BY PROJECT_ID, TOOL_ID, USER_ID;  

